<?php

namespace Torgovorot\TorgBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * SearchSeo
 */
class SearchSeo
{
    /**
     * @var string
     */
    private $searchStr;

    /**
     * @var integer
     */
    private $adsType;

    /**
     * @var integer
     */
    private $id;


    /**
     * Set searchStr
     *
     * @param string $searchStr
     * @return SearchSeo
     */
    public function setSearchStr($searchStr)
    {
        $this->searchStr = $searchStr;

        return $this;
    }

    /**
     * Get searchStr
     *
     * @return string 
     */
    public function getSearchStr()
    {
        return $this->searchStr;
    }

    /**
     * Set adsType
     *
     * @param integer $adsType
     * @return SearchSeo
     */
    public function setAdsType($adsType)
    {
        $this->adsType = $adsType;

        return $this;
    }

    /**
     * Get adsType
     *
     * @return integer 
     */
    public function getAdsType()
    {
        return $this->adsType;
    }

    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }
}
